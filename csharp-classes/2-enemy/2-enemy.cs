namespace Enemies
{
    /// <summary>
    /// Represents a zombie enemy.
    /// </summary>
    public class Zombie
    {
        /// <summary>
        /// Represents the health of the zombie.
        /// </summary>
        public int health;

        /// <summary>
        /// Constructor for the Zombie class.
        /// Health set to 0.
        /// </summary>
        public Zombie()
        {
            health = 0;
        }
    }
}